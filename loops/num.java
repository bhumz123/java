package loops;

public class num {
    public static void main(String[] args) {
        // to print the numbers
        for(int i=0;i<=10;i++)
        {
            System.out.println(i);
        }
        // now with the help of while loop
        int j=0;
        // condition for while loop
        while(j<=10){
            // this loop is executed till the condition is true
            System.out.println("hello world");
            j=j+1;
        }

        
        //now with the help of do while loops
        // in do while the loops is executed before
        // later the condition gets checked
        int k=0;
        do{
            System.out.println(k);
            k=k+1;
        }while(k<20);
        
    
    
        
    }
}


